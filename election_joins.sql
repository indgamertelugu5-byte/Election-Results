use election_data;
SELECT * FROM CONSTITUENCY;
SELECT * FROM PARTY;
SELECT * FROM RESULTS;
SELECT * FROM CANDIDATES;

/* LIST OF CANDIDATES THEIR CONSTITUENCY AND PARTY_NAME OF THE CONSTESTED CANDIDATE*/

SELECT C.CANDIDATE_NAME,CO.NAME AS CONSTITUENCY,P.PARTY_NAME,CO.DISTRICT FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID;

/*LIST OF TOP 3 MAJOR PARTIES IN STATE WITH  THEIR CONTESTING CANDIDATES */

SELECT C.CANDIDATE_NAME,CO.NAME AS CONSTITUENCY,P.PARTY_NAME FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID WHERE P.PARTY_NAME IN('INC','BJP','BRS');

/*LIST OF CANDIDATES,VOTES POLLED,RESULT ,CONSTITUENCY AND PARTYNAME IN THE DISTRICT*/

SELECT C.CANDIDATE_NAME,R.VOTES_POLLED AS TOTAL_VOTES_PER_CANDIDATE,R.STATUS AS RESULT,
CO.NAME AS CONSTITUENCY,P.PARTY_NAME FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID RIGHT JOIN RESULTS AS R
ON C.CANDIDATE_ID=R.CANDIDATE_ID;

/*LIST OF CANDIDATES WHOSE WON AND RUNNER UP IN EACH CONSTITUENCY IN ELECTIONS*/

SELECT C.CANDIDATE_NAME,R.VOTES_POLLED AS TOTAL_VOTES_PER_CANDIDATE,R.STATUS AS RESULT,
CO.NAME AS CONSTITUENCY,P.PARTY_NAME FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID RIGHT JOIN RESULTS AS R
ON C.CANDIDATE_ID=R.CANDIDATE_ID WHERE R.STATUS IN('WON','RUNNER UP');

/* RESULTS OF EACH CONSTITUENCY WITH CANDIDATE_NAME,VOTES_POLLED,RESULTS,CONSTITUENCY_NAME,PARTY_NAME*/

SELECT C.CANDIDATE_NAME,R.VOTES_POLLED AS TOTAL_VOTES_PER_CANDIDATE,R.STATUS AS RESULT,
CO.NAME AS CONSTITUENCY,P.PARTY_NAME  FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID RIGHT JOIN RESULTS AS R
ON C.CANDIDATE_ID=R.CANDIDATE_ID WHERE CO.NAME='RAMAGUNDAM';

SELECT C.CANDIDATE_NAME,R.VOTES_POLLED AS TOTAL_VOTES_PER_CANDIDATE,R.STATUS AS RESULT,
CO.NAME AS CONSTITUENCY,P.PARTY_NAME FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID RIGHT JOIN RESULTS AS R
ON C.CANDIDATE_ID=R.CANDIDATE_ID WHERE CO.NAME='MANTHANI';

SELECT C.CANDIDATE_NAME,R.VOTES_POLLED AS TOTAL_VOTES_PER_CANDIDATE,R.STATUS AS RESULT,
CO.NAME AS CONSTITUENCY,P.PARTY_NAME FROM CANDIDATES AS C 
LEFT JOIN CONSTITUENCY AS CO ON C.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
LEFT JOIN PARTY AS P ON C.PARTY_ID=P.PARTY_ID RIGHT JOIN RESULTS AS R
ON C.CANDIDATE_ID=R.CANDIDATE_ID WHERE CO.NAME='PEDDAPALLI';

/* LIST OF PARTIES ANF THEIR TOTAL VOTES POLLED IN OVERALL DISTRICT*/

SELECT P.PARTY_NAME,SUM(R.VOTES_POLLED) AS VOTES_PER_PARTY FROM RESULTS AS R LEFT JOIN PARTY AS P
ON R.PARTY_ID=P.PARTY_ID GROUP BY P.PARTY_NAME;

/*NAME OF CANDIDATE,PARTY,CONSTITUENCY OF WINNING CANDIDATE IN THE DISTRICT*/

SELECT C.CANDIDATE_NAME,P.PARTY_NAME,R.STATUS AS RESULT,CO.NAME AS CONSTITUENCY,CO.DISTRICT FROM RESULTS AS R
JOIN CANDIDATES AS C ON R.CANDIDATE_ID=C.CANDIDATE_ID JOIN PARTY AS  P 
ON C.PARTY_ID=P.PARTY_ID JOIN CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID WHERE R.STATUS='WON' ORDER BY CO.CONSTITUENCY_ID;

SELECT P.PARTY_NAME,COUNT(P.PARTY_NAME) AS NUMBER_OF_SEATS_PARTY_WON,CO.NAME AS CONSTITUENCY,CO.DISTRICT
FROM RESULTS AS R JOIN PARTY AS P
ON R.PARTY_ID=P.PARTY_ID JOIN CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE R.STATUS='WON' GROUP BY P.PARTY_NAME,CO.DISTRICT,CO.NAME;

/*NUMBER OF CONSTITUENCIES WON BY EACH PARTY IN THE DISTRICT
*/
SELECT P.PARTY_NAME,COUNT(*) AS TOTAL_SEATS_BY_PARTY_DISTRICT,CO.DISTRICT FROM RESULTS AS R
JOIN PARTY AS P ON R.PARTY_ID=P.PARTY_ID JOIN CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE R.STATUS = 'WON'
GROUP BY P.PARTY_NAME,CO.DISTRICT;

/*LIST OF PARTIES AS THE SECOND LEADING*/

SELECT P.PARTY_NAME,COUNT(P.PARTY_NAME) AS TOTAL_SEATS_BY_PARTY_DISTRICT,CO.DISTRICT FROM RESULTS AS R
JOIN PARTY AS P ON R.PARTY_ID=P.PARTY_ID JOIN CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE R.STATUS = 'RUNNER UP'
GROUP BY P.PARTY_NAME,CO.DISTRICT;

/*WINNINHG MAJORITY OF EACH CANDIDATE*/

SELECT R.CONSTITUENCY_ID,CO.NAME AS CONSTITUENCY,MAX(VOTES_POLLED)-(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID=1 AND VOTES_POLLED <
(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID =1)) AS WINNING_MAJORITY FROM RESULTS 
AS R JOIN CONSTITUENCY AS CO 
ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE CO.CONSTITUENCY_ID =1 GROUP BY CO.NAME;


SELECT R.CONSTITUENCY_ID,CO.NAME AS CONSTITUENCY,MAX(VOTES_POLLED)-(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID=2 AND VOTES_POLLED <
(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID =2)) AS WINNING_MAJORITY FROM RESULTS AS R JOIN CONSTITUENCY AS CO 
ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE CO.CONSTITUENCY_ID =2 GROUP BY CO.NAME;

SELECT R.CONSTITUENCY_ID,CO.NAME AS CONSTITUENCY,MAX(VOTES_POLLED)-(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID=3 AND VOTES_POLLED <
(SELECT MAX(VOTES_POLLED) FROM RESULTS WHERE CONSTITUENCY_ID =3)) AS WINNING_MAJORITY FROM RESULTS 
AS R JOIN CONSTITUENCY AS CO 
ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID
WHERE CO.CONSTITUENCY_ID =3 GROUP BY CO.NAME;

/*PERCENTAGE OF VOTES_POLLED IN EACH CONSTITUENCY WITH ACTUAL VOTERS OF CONSTITUENCY*/

SELECT CO.NAME AS CONSTITUENCY,SUM(R.VOTES_POLLED) AS TOTAL_POLL,(SELECT SUM(TOTAL_VOTERS) FROM CONSTITUENCY WHERE NAME='RAMAGUNDAM')
AS TOTAL_ELECTORS,(SUM(R.VOTES_POLLED)*100/(SELECT SUM(TOTAL_VOTERS) AS VOTING_PERCENTAGE FROM CONSTITUENCY WHERE NAME='RAMAGUNDAM')) 
AS DISTRICT_VOTING_PERCENTAGE FROM RESULTS AS R JOIN
CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID GROUP BY CO.NAME;

/*TOTAL VOTES POLLED ,ACTUAL NUMBER OF VOTES AND VOTING PERCENTAGE IN THE DISTRICT*/

SELECT SUM(R.VOTES_POLLED) AS TOTAL_POLL,(SELECT SUM(TOTAL_VOTERS) FROM CONSTITUENCY )AS TOTAL_ELECTORS,
(SUM(R.VOTES_POLLED)*100/(SELECT SUM(TOTAL_VOTERS) AS VOTING_PERCENTAGE FROM CONSTITUENCY)) 
AS DISTRICT_VOTING_PERCENTAGE FROM RESULTS AS R JOIN
CONSTITUENCY AS CO ON R.CONSTITUENCY_ID=CO.CONSTITUENCY_ID;













