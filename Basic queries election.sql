USE ELECTION_DATA;

SELECT * FROM CONSTITUENCY;

SELECT NAME AS CONSTITUENCY_NAME,TOTAL_VOTERS AS CONSTITUENCY_VOTERS FROM CONSTITUENCY;

SELECT NAME AS CONSTITUENCY, TOTAL_VOTERS AS MOST_VOTERS FROM CONSTITUENCY 
WHERE TOTAL_VOTERS=(SELECT MAX(TOTAL_VOTERS)  FROM CONSTITUENCY);

SELECT NAME AS CONSTITUENCY, TOTAL_VOTERS AS LEAST_VOTERS FROM CONSTITUENCY 
WHERE TOTAL_VOTERS=(SELECT MIN(TOTAL_VOTERS) FROM CONSTITUENCY);

SELECT DISTRICT,SUM(TOTAL_VOTERS) AS SUM_OF_VOTERS_IN_DISTRICT FROM CONSTITUENCY GROUP BY DISTRICT;

SELECT * FROM PARTY;

SELECT PARTY_NAME,PARTY_SYMBOL FROM PARTY;

SELECT COUNT(*) AS TOTAL_PARTIES_CONTESTED_IN_DISTRICT FROM PARTY;

SELECT PARTY_NAME FROM PARTY WHERE PARTY_NAME LIKE 'B%';

SELECT PARTY_SYMBOL FROM PARTY WHERE PARTY_SYMBOL LIKE '%A%';

SELECT * FROM CANDIDATES;

SELECT CONSTITUENCY_ID,CANDIDATE_NAME FROM CANDIDATES ORDER BY CANDIDATE_ID ;

SELECT CONSTITUENCY_ID,COUNT(*) AS MALE_COUNT FROM CANDIDATES WHERE GENDER ='MALE' 
GROUP BY CONSTITUENCY_ID;

SELECT  CONSTITUENCY_ID,COUNT(*) AS FEMALE_COUNT FROM CANDIDATES 
WHERE GENDER='FEMALE' GROUP BY CONSTITUENCY_ID ;

SELECT CONSTITUENCY_ID,COUNT(*) AS TOTAL_CANDIDATES FROM CANDIDATES GROUP BY CONSTITUENCY_ID; 

SELECT COUNT(*) AS TOTAL_CANDIDATES_DISTRICT FROM CANDIDATES;

SELECT * FROM RESULTS;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS;

SELECT CANDIDATE_ID,PARTY_ID,STATUS FROM RESULTS;

SELECT * FROM RESULTS WHERE STATUS='WON';

SELECT * FROM RESULTS WHERE STATUS='RUNNER UP';

SELECT PARTY_ID,SUM(VOTES_POLLED) AS PARTY_VOTES_IN_DISTRICT FROM RESULTS GROUP BY PARTY_ID;

SELECT SUM(VOTES_POLLED) AS TOTAL_VOTES_POLLED_DISTRICT FROM RESULTS;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS 
WHERE VOTES_POLLED<=5000 ORDER BY CANDIDATE_ID;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS 
WHERE VOTES_POLLED BETWEEN 5000 AND 20000 ORDER BY CANDIDATE_ID;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS
WHERE VOTES_POLLED >20000 AND VOTES_POLLED <=50000;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS
WHERE VOTES_POLLED >50000 AND VOTES_POLLED < 100000;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS
WHERE VOTES_POLLED >100000 ORDER BY CANDIDATE_ID;

SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS WHERE CONSTITUENCY_ID=1;
SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS WHERE CONSTITUENCY_ID=2;
SELECT CANDIDATE_ID,CONSTITUENCY_ID,PARTY_ID,VOTES_POLLED FROM RESULTS WHERE CONSTITUENCY_ID=3;



